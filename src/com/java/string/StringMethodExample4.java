package com.java.string;

public class StringMethodExample4 {
	public static void main(String args[]) {
		
		String s = "banglore";
		String s1 = "banglore";
		String s2 = "HYDERABAD";
		String s3 = "Noida";
		
		System.out.println(s.equalsIgnoreCase(s1));//true because content and case same
		System.out.println(s.equalsIgnoreCase(s2));//false because content is not same
		System.out.println(s2.equalsIgnoreCase(s3));//false content is not same
		
	
		
	
	}
	
		
	}
	


